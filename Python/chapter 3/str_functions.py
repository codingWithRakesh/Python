name = "rakesh"

print(len(name))  # Length of the string
print(name.endswith("sh"))  # Check if the string ends with "sh"
print(name.startswith("ra"))  # Check if the string starts with "ra"
print(name.upper())  # Convert to uppercase
print(name.lower())  # Convert to lowercase
print(name.replace("rakesh", "Rakesh"))  # Replace "rakesh" with "Rakesh"
print(name.find("ke"))  # Find the index of substring "ke"
print(name.isalpha())  # Check if all characters are alphabetic
print(name.isdigit())  # Check if all characters are digits     
print(name.isalnum())  # Check if all characters are alphanumeric
print(name.count("a"))  # Count occurrences of "a"
print(name.split("k"))  # Split the string at "k"
print(name[0:3])  # Slicing to get first three characters
print(name[-4:-1])  # Negative slicing to get characters from index -4 to
print(name[1:4])  # Slicing to get characters from index 1 to 3
print(name[:4])  # Slicing to get first four characters
print(name[1:])  # Slicing to get characters from index 1 to the end
print(name[1:5])  # Slicing to get characters from index 1 to 4
print(name.capitalize())  # Capitalize the first character
print(name.title())  # Convert to title case
print(name.swapcase())  # Swap case of all characters
print(name.strip())  # Remove leading and trailing whitespace